// @lc code=start
impl Solution {
    pub fn find_median_sorted_arrays(nums1: Vec<i32>, nums2: Vec<i32>) -> f64 {
        let mut merged: Vec<i32> = nums1.into_iter().chain(nums2.into_iter()).collect();
        merged.sort_unstable();
        let len = merged.len();
        if len % 2 == 0 {
            (merged[len / 2 - 1] as f64 + merged[len / 2] as f64) / 2.0
        } else {
            merged[len / 2] as f64
        }
    }
}
